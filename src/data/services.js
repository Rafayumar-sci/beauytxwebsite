// Helper to get all services from a category (flattened from groups)
export function getCategoryServices(category) {
  return category.groups.flatMap((g) => g.services);
}

// Flatten all services across all categories for booking/overview use
export const allServices = serviceCategories.flatMap((cat) =>
  cat.groups.flatMap((g) => g.services)
);

// Get the group that contains a specific service by slug
export function findServiceInCategory(category, serviceSlug) {
  for (const group of category.groups) {
    for (const service of group.services) {
      if (toSlug(service.name) === serviceSlug) {
        return { service, group };
      }
    }
  }
  return { service: null, group: null };
}

// Find a group by slug within a category
export function findGroupInCategory(category, groupSlug) {
  for (const group of category.groups) {
    if (toSlug(group.name) === groupSlug) {
      return group;
    }
  }
  return null;
}

function toSlug(text) {
  return text
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}
